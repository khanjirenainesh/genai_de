
 --================statement 1================ 
DROP TABLE IF EXISTS AU_WKS.WKS_METCASH_GROCERY_DATE;

 --================statement 2================ 
CREATE TABLE AU_WKS.WKS_METCASH_GROCERY_DATE AS
SELECT
  FILE_NAME,
  GROSS_SALES_WK1,
  GROSS_SALES_WK2,
  GROSS_SALES_WK3,
  GROSS_SALES_WK4,
  GROSS_SALES_WK5,
  GROSS_CASES_WK1,
  GROSS_CASES_WK2,
  GROSS_CASES_WK3,
  GROSS_CASES_WK4,
  GROSS_CASES_WK5
FROM AU_SDL.SDL_METCASH_IND_GROCERY
WHERE
  TRIM(SUPP_ID || SUPP_NAME || STATE || BANNER_ID || BANNER || CUSTOMER_ID || CUSTOMER) IS NULL
  AND (
    GROSS_SALES_WK1 LIKE '%/%'
    OR GROSS_SALES_WK2 LIKE '%/%'
    OR GROSS_SALES_WK3 LIKE '%/%'
    OR GROSS_SALES_WK4 LIKE '%/%'
    OR GROSS_SALES_WK5 LIKE '%/%'
  );
