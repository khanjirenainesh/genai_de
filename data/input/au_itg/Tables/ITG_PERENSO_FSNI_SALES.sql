
 --================statement 1================ 
DELETE FROM AU_ITG.ITG_PERENSO_FSNI_SALES
WHERE
  SPLIT_PART(FILE_NAME, '.', 1) = '"+((String)globalMap.get("name_from_file"))+"';

 --================statement 2================ 
INSERT INTO AU_ITG.ITG_PERENSO_FSNI_SALES
SELECT
  SITE,
  VENDOR_ID,
  VENDOR_NAME,
  DEPARTMENT,
  MERCHANDISE_GROUP,
  ARTICLE,
  ARTICLE_DESCRIPTION,
  RETAIL_BARCODE AS PROD_EAN,
  BANNER,
  STORE_NUMBER AS STORE_CD,
  STORE_NAME,
  BILLING_DOCUMENT AS BILL_DOC,
  TO_DATE(BILLING_DATE, 'DD.MM.YYYY') AS BILL_DT,
  TO_DATE(PRICING_DATE, 'DD.MM.YYYY') AS PRICING_DT,
  NET_SALES AS SLS_VALUE,
  QTY_IN_SALES AS SLS_QTY,
  SALES_UOM,
  QTY_IN_BASE AS BASE_QTY,
  BASE_UOM,
  'NZD' AS CRNCY,
  FILE_NAME,
  CURRENT_TIMESTAMP() AS CRT_DT,
  CURRENT_TIMESTAMP() AS UPDT_DT
FROM AU_SDL.SDL_PERENSO_FSNI_SALES;
